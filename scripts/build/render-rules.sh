#!/usr/bin/env bash
set -euo pipefail

repo_root="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
mode="${1:-}"

# shellcheck source=scripts/lib/globals-data.sh
source "${repo_root}/scripts/lib/globals-data.sh"

case "${mode}" in
  --check|"") ;;
  *)
    echo "usage: $0 [--check]" >&2
    exit 2
    ;;
esac

render_target() {
  local target="$1"
  shift
  local tmp
  tmp="$(mktemp)"

  {
    echo "# Generated Harness Rules"
    echo
    echo "Generated from \`globals/rules/shared/\` and harness-specific \`globals/rules/<harness>/\` fragments."
    echo "Do not edit this file directly; edit rule fragments and run \`./scripts/build/render-rules.sh\`."
    echo

    local dir fragment
    for dir in "$@"; do
      for fragment in "${repo_root}/${dir}"/*.md; do
        [[ -e "${fragment}" ]] || continue
        printf '\n'
        sed '/^$/N;/^\n$/D' "${fragment}"
        printf '\n'
      done
    done
  } >"${tmp}"

  if [[ "${mode}" == "--check" ]]; then
    if cmp -s "${tmp}" "${repo_root}/${target}"; then
      echo "ok: ${target} generated rules current"
      rm -f "${tmp}"
      return 0
    fi
    echo "fail: ${target} generated rules drifted" >&2
    diff -u "${repo_root}/${target}" "${tmp}" >&2 || true
    rm -f "${tmp}"
    return 1
  fi

  mv "${tmp}" "${repo_root}/${target}"
  echo "render: ${target}"
}

while IFS=$'\t' read -r target source_dirs_csv; do
  IFS=',' read -ra source_dirs <<< "${source_dirs_csv}"
  render_target "${target}" "${source_dirs[@]}"
done < <(rule_target_rows)
