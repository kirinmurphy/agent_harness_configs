#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { repoRoot } from "../cli/paths.mjs";
import { listSourceSkills } from "../cli/skill-lib.mjs";

const SKILL_MANIFEST_REL = "manifests/skill-invocation.json";
const COMMAND_MANIFEST_REL = "manifests/slash-commands.json";
const GENERATED_MARKER = "Generated by scripts/build/render-slash-commands.mjs; do not edit directly.";
const LEGACY_GENERATED_MARKER = "Generated by scripts/build/render-skill-commands.mjs; do not edit directly.";
const HARNESSES = {
  claude: {
    dir: "globals/claude/commands",
    skillPath: (skill) => `~/.claude/skills/${skill}/SKILL.md`,
  },
  codex: {
    dir: "globals/codex/commands",
    skillPath: (skill) => `~/.agents/skills/${skill}/SKILL.md`,
  },
};

const args = new Set(process.argv.slice(2));
for (const arg of args) {
  if (arg !== "--check" && arg !== "--quiet" && arg !== "-q") {
    console.error("usage: scripts/build/render-slash-commands.mjs [--check] [--quiet|-q]");
    process.exit(2);
  }
}

const checkOnly = args.has("--check");
const quiet = args.has("--quiet") || args.has("-q");

function readJson(relPath) {
  return JSON.parse(fs.readFileSync(path.join(repoRoot, relPath), "utf8"));
}

function readText(relPath) {
  return fs.readFileSync(path.join(repoRoot, relPath), "utf8").replace(/\r\n/g, "\n");
}

function readIfExists(filePath) {
  try {
    return fs.readFileSync(filePath, "utf8").replace(/\r\n/g, "\n");
  } catch (err) {
    if (err?.code === "ENOENT") return null;
    throw err;
  }
}

function commandTarget(name) {
  return `${name}.md`;
}

function validateName(name, label) {
  if (!/^[a-z0-9][a-z0-9-]*$/.test(name)) {
    throw new Error(`${label}: invalid name "${name}"`);
  }
}

function validateSkillManifest(manifest, sourceSkills) {
  if (!manifest || !Array.isArray(manifest.skills)) throw new Error(`${SKILL_MANIFEST_REL}: missing skills array`);

  const seen = new Set();
  const policies = new Map();
  for (const entry of manifest.skills) {
    const skill = entry?.skill;
    if (typeof skill !== "string" || skill.trim() === "") throw new Error(`${SKILL_MANIFEST_REL}: skill must be a string`);
    if (seen.has(skill)) throw new Error(`${SKILL_MANIFEST_REL}: duplicate skill entry: ${skill}`);
    seen.add(skill);
    if (!sourceSkills.has(skill)) throw new Error(`${SKILL_MANIFEST_REL}: unknown skill: ${skill}`);
    if (!["low", "medium", "high"].includes(entry.risk)) {
      throw new Error(`${SKILL_MANIFEST_REL}: ${skill} risk must be low, medium, or high`);
    }
    if (!["auto", "manual"].includes(entry.invocation)) {
      throw new Error(`${SKILL_MANIFEST_REL}: ${skill} invocation must be auto or manual`);
    }
    if (typeof entry.explicit_command !== "boolean") {
      throw new Error(`${SKILL_MANIFEST_REL}: ${skill} explicit_command must be true or false`);
    }
    policies.set(skill, { explicitCommand: entry.explicit_command });
  }

  for (const skill of sourceSkills) {
    if (!seen.has(skill)) throw new Error(`${SKILL_MANIFEST_REL}: missing skill entry: ${skill}`);
  }

  return policies;
}

function withGeneratedMarker(content) {
  const marker = `<!-- ${GENERATED_MARKER} -->`;
  if (content.includes(marker)) return content.endsWith("\n") ? content : `${content}\n`;

  const lines = content.split("\n");
  if (lines[0] === "---") {
    const end = lines.findIndex((line, index) => index > 0 && line === "---");
    if (end > 0) {
      lines.splice(end + 1, 0, "", marker);
      return `${lines.join("\n").replace(/\n*$/, "")}\n`;
    }
  }
  return `${marker}\n\n${content.replace(/\n*$/, "")}\n`;
}

function skillBackedCommand(command, harness) {
  return withGeneratedMarker(`---
description: ${command.description}
---

# /${command.name}

Use the \`${command.skill}\` skill for this request.

Read \`${harness.skillPath(command.skill)}\`, then follow its workflow.

Keep the skill as the source of truth; this command is only the explicit entry
point.
`);
}

function standaloneCommand(command) {
  return withGeneratedMarker(readText(command.source));
}

function validateCommands(manifest, sourceSkills, skillPolicies) {
  if (!manifest || !Array.isArray(manifest.commands)) throw new Error(`${COMMAND_MANIFEST_REL}: missing commands array`);

  const seen = new Set();
  const commandBackedSkills = new Set();
  const commands = [];

  for (const command of manifest.commands) {
    const name = String(command?.name ?? "").replace(/^\//, "");
    validateName(name, COMMAND_MANIFEST_REL);
    if (seen.has(name)) throw new Error(`${COMMAND_MANIFEST_REL}: duplicate command: ${name}`);
    seen.add(name);

    const kind = command.kind;
    if (!["skill-backed", "standalone"].includes(kind)) {
      throw new Error(`${COMMAND_MANIFEST_REL}: ${name} kind must be skill-backed or standalone`);
    }
    const description = String(command.description ?? "").trim();
    if (description === "" || description.includes("\n")) {
      throw new Error(`${COMMAND_MANIFEST_REL}: ${name} needs a one-line description`);
    }
    const harnesses = command.harnesses;
    if (!Array.isArray(harnesses) || harnesses.length === 0) {
      throw new Error(`${COMMAND_MANIFEST_REL}: ${name} needs at least one harness`);
    }
    for (const harness of harnesses) {
      if (!HARNESSES[harness]) throw new Error(`${COMMAND_MANIFEST_REL}: ${name} unknown harness: ${harness}`);
    }

    if (kind === "skill-backed") {
      if (!sourceSkills.has(command.skill)) {
        throw new Error(`${COMMAND_MANIFEST_REL}: ${name} references unknown skill: ${command.skill}`);
      }
      if (!skillPolicies.get(command.skill)?.explicitCommand) {
        throw new Error(`${COMMAND_MANIFEST_REL}: ${name} references ${command.skill}, but ${SKILL_MANIFEST_REL} has explicit_command=false`);
      }
      commandBackedSkills.add(command.skill);
      commands.push({ name, kind, description, skill: command.skill, harnesses });
      continue;
    }

    if (typeof command.source !== "string" || command.source.trim() === "") {
      throw new Error(`${COMMAND_MANIFEST_REL}: ${name} standalone command needs source`);
    }
    const sourcePath = path.join(repoRoot, command.source);
    if (!fs.existsSync(sourcePath)) throw new Error(`${COMMAND_MANIFEST_REL}: ${name} source missing: ${command.source}`);
    commands.push({ name, kind, description, source: command.source, harnesses });
  }

  for (const [skill, policy] of skillPolicies) {
    if (policy.explicitCommand && !commandBackedSkills.has(skill)) {
      throw new Error(`${SKILL_MANIFEST_REL}: ${skill} has explicit_command=true but no skill-backed slash command`);
    }
  }

  return commands.sort((a, b) => a.name.localeCompare(b.name));
}

function expectedCommands(commands) {
  const byHarness = new Map(Object.keys(HARNESSES).map((name) => [name, new Map()]));

  for (const command of commands) {
    for (const harnessName of command.harnesses) {
      const harness = HARNESSES[harnessName];
      const content = command.kind === "skill-backed" ? skillBackedCommand(command, harness) : standaloneCommand(command);
      byHarness.get(harnessName).set(commandTarget(command.name), content);
    }
  }

  return byHarness;
}

function hasGeneratedMarker(filePath) {
  const content = readIfExists(filePath);
  return content !== null && (content.includes(GENERATED_MARKER) || content.includes(LEGACY_GENERATED_MARKER));
}

function renderHarness(harnessName, expected) {
  const outDir = path.join(repoRoot, HARNESSES[harnessName].dir);
  let changed = 0;
  let failed = 0;

  if (!checkOnly) fs.mkdirSync(outDir, { recursive: true });

  for (const [fileName, content] of expected) {
    const filePath = path.join(outDir, fileName);
    const existing = readIfExists(filePath);
    if (existing !== null && existing !== content && !hasGeneratedMarker(filePath)) {
      console.error(`fail: ${HARNESSES[harnessName].dir}/${fileName} exists and is not generated`);
      failed++;
      continue;
    }
    if (existing === content) continue;
    if (checkOnly) {
      console.error(`stale: ${HARNESSES[harnessName].dir}/${fileName}`);
      failed++;
      continue;
    }
    fs.writeFileSync(filePath, content);
    changed++;
    if (!quiet) console.log(`render: ${HARNESSES[harnessName].dir}/${fileName}`);
  }

  if (fs.existsSync(outDir)) {
    for (const fileName of fs.readdirSync(outDir)) {
      if (!fileName.endsWith(".md") || expected.has(fileName)) continue;
      const filePath = path.join(outDir, fileName);
      if (!hasGeneratedMarker(filePath)) continue;
      if (checkOnly) {
        console.error(`stale generated command: ${HARNESSES[harnessName].dir}/${fileName}`);
        failed++;
        continue;
      }
      fs.unlinkSync(filePath);
      changed++;
      if (!quiet) console.log(`prune: ${HARNESSES[harnessName].dir}/${fileName}`);
    }
  }

  return { changed, failed };
}

function main() {
  const sourceSkills = new Set(listSourceSkills(path.join(repoRoot, "globals", "agents", "skills")));
  const skillPolicies = validateSkillManifest(readJson(SKILL_MANIFEST_REL), sourceSkills);
  const commands = validateCommands(readJson(COMMAND_MANIFEST_REL), sourceSkills, skillPolicies);
  const expected = expectedCommands(commands);

  let changed = 0;
  let failed = 0;
  for (const [harnessName, harnessExpected] of expected) {
    const result = renderHarness(harnessName, harnessExpected);
    changed += result.changed;
    failed += result.failed;
  }

  if (failed > 0) {
    console.error(`slash command render ${checkOnly ? "check " : ""}failed (${failed} issue(s))`);
    process.exit(1);
  }
  if (!quiet) {
    const mode = checkOnly ? "checked" : "rendered";
    console.log(`${commands.length} slash command(s) ${mode}, ${changed} file(s) changed`);
  }
}

try {
  main();
} catch (err) {
  console.error(err?.stack || String(err));
  process.exit(1);
}
